def need_input(node):
        return node.is_terminal or len(node.children) >= 2    


def dfs(node, depth):
        total = 0
        if node.is_terminal:
            total += depth

        for char in node.children:
            child = node.children[char]
            if need_input(node):
                total += dfs(child, depth + 1)
            else:
                total += dfs(child, depth)
        return total

