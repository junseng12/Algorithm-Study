import sys
from collections import defaultdict
input = sys.stdin.readline

sys.setrecursionlimit(10**6)

# ê·¸ë˜í”„ êµ¬ì„±
graph = defaultdict(list)

N = int(input())

#ê° ë…¸ë“œì˜ ë¶€ëª¨ ë…¸ë“œë¥¼ ê¸°ì–µí•˜ê³  ìˆëŠ” ë¦¬ìŠ¤íŠ¸
parent = list(map(int, input().split()))

# ì´ì „ ê·¸ë˜í”„ ì—°ê²°ì—ì„œëŠ” ì–‘ë°©í–¥ ì—°ê±¸ (í•˜ì§€ë§Œ, ì¼ë°˜ì ì¸ íŠ¸ë¦¬ì—ì„œëŠ” ë¶€ëª¨ -> ìì‹ ë‹¨ë°©í–¥ êµ¬ì¡°)
for child in range(N): 
    if parent[child] != -1:
        graph[parent[child]].append(child)  # ë¶€ëª¨ â†’ ìì‹ë§Œ ì €ì¥


target = int(input())

leaf_count = 0

root = parent.index(-1)


def dfs(node) :
    global leaf_count
    if node == target:  # âŒ ì‚­ì œëœ ë…¸ë“œëŠ” íƒìƒ‰í•˜ì§€ ì•ŠìŒ
        return
    
    #ìì‹ ë…¸ë“œê°€ ì—†ëŠ” ê²½ìš° = ë¦¬í”„ ë…¸ë“œ
    if not graph[node]:
        leaf_count += 1
        return
    
    is_leaf = True
    for child in graph[node]:
        if child != target:
            dfs(child)
            is_leaf = False
            
    if is_leaf:
        leaf_count += 1
        
        
if target != root :
    dfs(root)
    print(leaf_count)
else :
    print(0)
